public class Calculadora extends Operacoes {
    /**
     *
     * @param a
     * @param b
     * @param c
     * @return params sum
     */
    public double somar(final double a, final double b, final double c) {
        return a + b + c;
    }

    /**
     *
     * @param a
     * @param b
     * @param c
     * @return sum of all params
     */
    public float somar(final float a, final float b, final float c) {
        return a + b + c;
    }
}
